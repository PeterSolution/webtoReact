        <h1>SQL</h1><br />
        <h2>Moja droga z SQL</h2><br />
        <h2>Wiem jak zarządzać bazą danych przy pomocy czystego SQL oraz umiem zastosować to w kodzie zarówno z zasadom "Code first" jak i "Database first". W aplikacjach serwerowych w C# pisałem połączenia używając code first przy użyciu entity framework</h2><br />
          <p>SQL (Structured Query Language) to deklaratywny język programowania zaprojektowany do zarządzania danymi w relacyjnych bazach danych. Jego główne cechy to:</p>
          <ul>
            <li>Standardizacja – SQL jest standardem ANSI/ISO, choć różne systemy baz danych mogą mieć swoje dialekty</li>
            <li>Deklaratywność – użytkownik określa, jakie dane chce uzyskać, a nie jak je uzyskać</li>
            <li>Podzielony na podgrupy: DDL (Data Definition Language), DML (Data Manipulation Language), DCL (Data Control Language)</li>
            <li>Obsługa transakcji zapewniająca integralność danych</li>
            <li>Możliwość tworzenia złożonych zapytań poprzez łączenie tabel i używanie funkcji agregujących</li>
          </ul>
          <p>SQL umożliwia efektywne zarządzanie dużymi ilościami danych strukturalnych.</p>

